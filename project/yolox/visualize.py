def draw_boxes(tensor, output, cls_conf=0.60):
    B, C, H, W = tensor.size()
    if output is None:
        return tensor

    # output.size() -- [4, 7]
    image = ToPILImage()(tensor.squeeze(0))
    draw = ImageDraw.Draw(image)
    output = output.cpu()

    # (x1, y1, x2, y2, obj_score*class_score, class_id)
    boxes = output[:, 0:4]
    cls_score = output[:, 4]
    cls_ids = output[:, 5]

    for i in range(len(boxes)):
        box = boxes[i]
        cls_id = int(cls_ids[i])
        score = cls_score[i]

        if score < cls_conf:
            continue

        x0 = int(box[0])
        y0 = int(box[1])
        x1 = int(box[2])
        y1 = int(box[3])

        color = tuple((_COLORS[cls_id] * 255).astype(np.uint8).tolist())
        text = "{}:{:.1f}%".format(_NAMES[cls_id], score * 100)
        txt_color = (0, 0, 0) if np.mean(_COLORS[cls_id]) > 0.5 else (255, 255, 255)

        shape = [(x0, y0), (x1, y1)]
        draw.rectangle(shape, fill=None, outline=color)
        draw.text((x0, y0), text, fill=txt_color)

    image = ToTensor()(image)
    return image.unsqueeze(0)

